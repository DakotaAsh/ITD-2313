r=float(input("Enter the radius of the sphere: "))
diam=2**r
circ=diam*3.14
surfa=4*3.14*(r**2)
volu=(4/3)*3.14*(r**3)
print("The sphere's diameter is: ", diam)
print("The sphere's circumference is: ", circ)
print("The sphere's surface area is: ",surfa)
print("The sphere's volume is: ", volu)
